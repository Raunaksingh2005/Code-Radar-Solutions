#include <stdio.h>

int main() {
    int a;  // Declare variable to store the first integer
    int b;  // Declare variable to store the second integer

    // Read the first integer
    scanf("%d", &a);  

    // Read the second integer
    scanf("%d", &b);  

    // Output the sum of the two integers
    printf("sum: %d", a + b);  

    return 0;  // Program ends successfully
}
